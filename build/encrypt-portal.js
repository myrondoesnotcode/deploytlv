#!/usr/bin/env node
/*
 * encrypt-portal.js — Encrypt a Deploy TLV event portal's attendee data.
 *
 * The public GitHub Pages site must NEVER serve attendee data in plaintext
 * (the old `data.js` was fetchable by anyone via `curl`). This script takes
 * the local plaintext data file(s) for an event and produces an encrypted
 * `data.enc.js` — AES-256-GCM, key derived from a shared passphrase via
 * PBKDF2-SHA256. Only `data.enc.js` is committed/served; the plaintext stays
 * local (and gitignored). The browser decrypts in `crypto-gate.js` using the
 * exact same parameters (WebCrypto SubtleCrypto).
 *
 * Usage:
 *   DEPLOY_GATE_PASSWORD='your-passphrase' node build/encrypt-portal.js --event 3
 *   DEPLOY_GATE_PASSWORD='your-passphrase' node build/encrypt-portal.js --event 2
 *
 * The passphrase is NEVER written to disk or committed. Distribute it to
 * attendees out-of-band (the same way `pinsker` was shared).
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');
const crypto = require('crypto');

const PBKDF2_ITERATIONS = 250000; // ~0.1s/guess in-browser; slows offline brute force
const SALT_BYTES = 16;
const IV_BYTES = 12; // AES-GCM standard nonce size
const KEY_BYTES = 32; // AES-256

// Per-event config: which plaintext files feed the data global, and the
// window global name the app reads at runtime.
// `raw: true` → encrypt the file's JS text verbatim (data + precompiled app),
// decrypted-and-eval'd in-browser. Otherwise → run the file(s), JSON-encode the
// assembled window global, and encrypt that.
const EVENTS = {
  '1': { global: 'D1', raw: true, files: ['events/1/data.js'] },
  '2': { global: 'D2', files: ['events/2/data1.js', 'events/2/data2.js'] },
  '3': { global: 'D3', files: ['events/3/data.js'] },
};

function parseArgs(argv) {
  const args = {};
  for (let i = 2; i < argv.length; i++) {
    if (argv[i] === '--event') args.event = argv[++i];
  }
  return args;
}

// Run the plaintext data file(s) in a sandbox with a `window` shim, then read
// back the assembled data global (e.g. window.D3). This mirrors exactly how the
// browser assembles the object, so we encrypt the same structure the app expects.
function loadDataGlobal(repoRoot, cfg) {
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  for (const rel of cfg.files) {
    const abs = path.join(repoRoot, rel);
    const code = fs.readFileSync(abs, 'utf8');
    vm.runInContext(code, sandbox, { filename: rel });
  }
  const data = sandbox.window[cfg.global];
  if (data == null) {
    throw new Error(`Data global window.${cfg.global} was not set by ${cfg.files.join(', ')}`);
  }
  return data;
}

function encrypt(plaintext, password) {
  const salt = crypto.randomBytes(SALT_BYTES);
  const iv = crypto.randomBytes(IV_BYTES);
  const key = crypto.pbkdf2Sync(password, salt, PBKDF2_ITERATIONS, KEY_BYTES, 'sha256');

  const cipher = crypto.createCipheriv('aes-256-gcm', key, iv);
  const ct = Buffer.concat([cipher.update(plaintext, 'utf8'), cipher.final()]);
  const tag = cipher.getAuthTag();

  // WebCrypto expects the GCM auth tag appended to the ciphertext.
  const ctWithTag = Buffer.concat([ct, tag]);

  return {
    v: 1,
    kdf: 'PBKDF2-SHA256',
    iter: PBKDF2_ITERATIONS,
    salt: salt.toString('base64'),
    iv: iv.toString('base64'),
    ct: ctWithTag.toString('base64'),
  };
}

function main() {
  const args = parseArgs(process.argv);
  const event = args.event;
  const cfg = EVENTS[event];
  if (!cfg) {
    console.error(`Usage: DEPLOY_GATE_PASSWORD=... node build/encrypt-portal.js --event <${Object.keys(EVENTS).join('|')}>`);
    process.exit(1);
  }

  const password = process.env.DEPLOY_GATE_PASSWORD;
  if (!password) {
    console.error('ERROR: set DEPLOY_GATE_PASSWORD in the environment (never commit it).');
    process.exit(1);
  }
  if (password.length < 6) {
    console.error('ERROR: passphrase too short — use something with real entropy (3-4 random words recommended).');
    process.exit(1);
  }

  const repoRoot = path.resolve(__dirname, '..');
  let plaintext;
  if (cfg.raw) {
    // Encrypt the JS bundle verbatim (browser will eval it after decrypt).
    plaintext = fs.readFileSync(path.join(repoRoot, cfg.files[0]), 'utf8');
  } else {
    plaintext = JSON.stringify(loadDataGlobal(repoRoot, cfg));
  }

  const payload = encrypt(plaintext, password);

  const outRel = `events/${event}/data.enc.js`;
  const outAbs = path.join(repoRoot, outRel);
  const banner = `/* Deploy #${event} — ENCRYPTED attendee data (AES-256-GCM). Generated by build/encrypt-portal.js.\n` +
    `   Contains NO plaintext. Decrypted in-browser by crypto-gate.js with the shared passphrase. */\n`;
  const js = `${banner}window.D${event}_ENC = ${JSON.stringify(payload)};\n`;
  fs.writeFileSync(outAbs, js);

  const kb = (Buffer.byteLength(plaintext) / 1024).toFixed(1);
  console.log(`✓ Encrypted window.${cfg.global} (${kb} KB plaintext) → ${outRel}`);
  console.log(`  PBKDF2 iterations: ${PBKDF2_ITERATIONS}, salt+iv random per build.`);
}

main();
