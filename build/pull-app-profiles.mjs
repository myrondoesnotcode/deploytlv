#!/usr/bin/env node
/**
 * Pull member profile edits made in the Deploy HQ app (Supabase `profiles`)
 * back into the site:
 *   1. writes build/profile-overrides.json  (the durable merge layer —
 *      parse-chat.mjs applies it on every rebuild, so chat re-exports never
 *      clobber app edits)
 *   2. applies the overrides to members/data/people.js in place, so you can
 *      publish without re-running the chat pipeline
 *
 * Run:  SUPABASE_ACCESS_TOKEN=sbp_… node build/pull-app-profiles.mjs
 * Then review `git diff members/data/people.js` and push to publish.
 *
 * Only fields editable in the app are synced; insight/cluster/revenue stay
 * curated by the site pipeline.
 */
import fs from 'node:fs';
import path from 'node:path';

const ROOT = path.join(import.meta.dirname, '..');
const PROJECT_REF = 'uzloavbzhebsoizkxheb';
const APP_EDITABLE = ['project', 'tagline', 'seeking', 'building', 'bg', 'linkedin', 'website', 'stack'];

// Two auth paths:
//  - PROFILE_SYNC_KEY (used by the GitHub Action): calls the key-gated
//    app_profile_overrides() RPC with the public anon key. The sync key can
//    read only these dossier fields — nothing else.
//  - SUPABASE_ACCESS_TOKEN (manual/local runs): management-API query.
const ANON_KEY =
  'eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9.eyJpc3MiOiJzdXBhYmFzZSIsInJlZiI6InV6bG9hdmJ6aGVic29pemt4aGViIiwicm9sZSI6ImFub24iLCJpYXQiOjE3ODM0NDEyNjAsImV4cCI6MjA5OTAxNzI2MH0.J0Aa8FYCVQYUddvDEbeN_0yNYweYs_nupsQu1MLLlnk';

let rows;
if (process.env.PROFILE_SYNC_KEY) {
  const res = await fetch(`https://${PROJECT_REF}.supabase.co/rest/v1/rpc/app_profile_overrides`, {
    method: 'POST',
    headers: { apikey: ANON_KEY, Authorization: `Bearer ${ANON_KEY}`, 'Content-Type': 'application/json' },
    body: JSON.stringify({ sync_key: process.env.PROFILE_SYNC_KEY }),
  });
  if (!res.ok) {
    console.error(`Supabase RPC failed: ${res.status} ${await res.text()}`);
    process.exit(1);
  }
  rows = await res.json();
} else if (process.env.SUPABASE_ACCESS_TOKEN) {
  const res = await fetch(`https://api.supabase.com/v1/projects/${PROJECT_REF}/database/query`, {
    method: 'POST',
    headers: { Authorization: `Bearer ${process.env.SUPABASE_ACCESS_TOKEN}`, 'Content-Type': 'application/json' },
    body: JSON.stringify({
      query: `select slug, name, ${APP_EDITABLE.join(', ')} from profiles where slug is not null and claimed_at is not null`,
    }),
  });
  if (!res.ok) {
    console.error(`Supabase query failed: ${res.status} ${await res.text()}`);
    process.exit(1);
  }
  rows = await res.json();
} else {
  console.error('set PROFILE_SYNC_KEY (CI) or SUPABASE_ACCESS_TOKEN (manual) — see build/README or AGENTS.md');
  process.exit(1);
}

// 1) The durable overrides file (keyed by slug).
const overrides = {};
for (const r of rows) {
  const o = {};
  for (const f of APP_EDITABLE) {
    if (r[f] != null && r[f] !== '' && (!Array.isArray(r[f]) || r[f].length)) o[f] = r[f];
  }
  if (Object.keys(o).length) overrides[r.slug] = o;
}
const overridesPath = path.join(ROOT, 'build', 'profile-overrides.json');
fs.writeFileSync(
  overridesPath,
  JSON.stringify(
    { _comment: 'App-sourced profile edits, keyed by slug. Regenerated by build/pull-app-profiles.mjs — do not edit by hand.', overrides },
    null,
    1
  ) + '\n'
);
console.log(`wrote ${Object.keys(overrides).length} member override(s) → build/profile-overrides.json`);

// 2) Apply to people.js in place.
const peoplePath = path.join(ROOT, 'members', 'data', 'people.js');
const src = fs.readFileSync(peoplePath, 'utf8');
const start = src.indexOf('window.MEM.people = ');
const jsonText = src.slice(start + 'window.MEM.people = '.length).replace(/;\s*$/, '');
const people = JSON.parse(jsonText);

let touched = 0;
for (const p of people) {
  const o = overrides[p.slug];
  if (!o) continue;
  p.profile = { ...p.profile, ...o };
  touched++;
}

const header = src.slice(0, start);
fs.writeFileSync(peoplePath, header + 'window.MEM.people = ' + JSON.stringify(people, null, 1) + ';\n');
console.log(`patched ${touched} profile(s) in members/data/people.js — review with git diff, then push to publish`);
